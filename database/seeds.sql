-- SAMPLE ATTENDANCE (last 30 days for all employees)
INSERT INTO attendance (employee_id, date, check_in, check_out, status, location)
SELECT
  e.id,
  d::date,
  CASE WHEN EXTRACT(DOW FROM d) NOT IN (0,6)
    THEN d + INTERVAL '9 hours' + (floor(random()*30)::int * INTERVAL '1 minute')
    ELSE NULL END,
  CASE WHEN EXTRACT(DOW FROM d) NOT IN (0,6)
    THEN d + INTERVAL '18 hours' + (floor(random()*60)::int * INTERVAL '1 minute')
    ELSE NULL END,
  CASE
    WHEN EXTRACT(DOW FROM d) IN (0,6) THEN 'weekend'
    WHEN random() < 0.05             THEN 'absent'
    WHEN random() < 0.1              THEN 'on_leave'
    ELSE 'present'
  END,
  'office'
FROM employees e,
     generate_series(CURRENT_DATE - INTERVAL '30 days', CURRENT_DATE, '1 day') AS d
ON CONFLICT (employee_id, date) DO NOTHING;